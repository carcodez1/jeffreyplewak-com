function runGit(args) {
  return execFileSync("git", args, {
    cwd: repoRoot,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "ignore"],
  }).trim();
}

function unique(items) {
  return [...new Set(items)];
}

function collectChangedFiles() {
  const diffFiles = runGit(["diff", "--name-only", "--relative"]);
  const cachedFiles = runGit(["diff", "--name-only", "--cached", "--relative"]);
  const untrackedFiles = runGit(["ls-files", "--others", "--exclude-standard"]);

  return unique(
    [diffFiles, cachedFiles, untrackedFiles]
      .flatMap((block) => block.split("\n"))
      .map((file) => file.trim())
      .filter(Boolean),
  );
}

const changedFiles = collectChangedFiles();
const changedSet = new Set(changedFiles);
const knownByPath = new Map(KNOWN_SURFACES.map((entry) => [entry.path, entry]));

const knownFindings = KNOWN_SURFACES.filter((entry) => changedSet.has(entry.path));
const investigateFindings = changedFiles
  .filter((file) => {
    if (knownByPath.has(file)) return false;
    return (
      file.startsWith("public/downloads/") ||
      file.startsWith("artifacts/") ||
      file === "next-env.d.ts"
    );
  })
  .map((path) => ({
    path,
    classification: "investigate",
    reason: "Generated-looking surface not yet classified in scripts/codex/check-generated-churn.mjs.",
    actions: [
      "Inspect the generator or source of truth for this file before committing it.",
      "Add an explicit classification to scripts/codex/check-generated-churn.mjs if this file is a recurring surface.",
    ],
  }));

const findings = [...knownFindings, ...investigateFindings];

if (findings.length === 0) {
  process.stdout.write("No known generated-artifact churn detected in the current worktree.\n");
  process.exit(0);
}

const counts = findings.reduce(
  (acc, item) => {
    acc[item.classification] = (acc[item.classification] ?? 0) + 1;
    return acc;
  },
  { durable: 0, restorable: 0, investigate: 0 },
);

process.stdout.write("Generated artifact findings:\n");
for (const item of findings) {
  process.stdout.write(`- ${item.path}\n`);
  process.stdout.write(`  classification: ${item.classification}\n`);
  process.stdout.write(`  reason: ${item.reason}\n`);
  for (const action of item.actions) {
    process.stdout.write(`  next: ${action}\n`);
  }
}

process.stdout.write("\nSummary:\n");
process.stdout.write(`- durable: ${counts.durable}\n`);
process.stdout.write(`- restorable: ${counts.restorable}\n`);
process.stdout.write(`- investigate: ${counts.investigate}\n`);
